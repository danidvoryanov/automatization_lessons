package Lesson_3.example_1;

public class Calculator {
    static int summ(int a, int b) {
        return a + b;
    }

    static int substract(int c, int d) {
        return c - d;
    }

    static int multiply(int i, int f) {
        return i * f;
    }

    static int divide(int g, int h) {
        return g / h;
    }
}
